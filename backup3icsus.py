def percapita(df, pop_capitais):

    # extrai código IBGE da capital
    df["codigo"] = df["Capital"].str.extract(r"(\d{6})")

    # dataframe da população
    pop_df = pd.DataFrame({
        "codigo": list(pop_capitais.keys()),
        "pop": list(pop_capitais.values())
    })

    # merge pelo código
    df = df.merge(pop_df, on="codigo", how="inner")

    # remove colunas que não são números
    df_num = df.drop(columns=["Capital","Total","pop","codigo"], errors="ignore")

    # limpa valores
    df_num = df_num.replace("-",0).astype(float)

    # calcula per capita
    df_num = df_num.div(df["pop"], axis=0) * 100000

    # usa codigo como índice
    df_num.index = df["codigo"]

    return df_num

def plot_linhas(dfs, nome_df, capitais):

    df = dfs[nome_df]

    fig = go.Figure()

    for i in range(len(df)):

        codigo = df.index[i]
        nome = capitais.get(codigo, codigo)

        fig.add_trace(
            go.Scatter(
                x=df.columns,
                y=df.iloc[i],
                mode="lines",
                name=nome
            )
        )

    fig.update_layout(
        title=f"DADOS SUSU - {nome_df}",
        xaxis_title="Tempo",
        yaxis_title="Atendimentos por 100k hab",
        width=1200,
        height=700
    )

    return fig

def plot_barras(dfs, nome_df, capitais):

    df = dfs[nome_df]

    fig = go.Figure()

    for i in range(len(df)):

        codigo = str(df.index[i])
        nome = capitais.get(codigo, codigo)

        fig.add_trace(
            go.Bar(
                x=df.columns,
                y=df.iloc[i],
                name=nome,
                visible=(i == 0)
            )
        )

    buttons = []

    for i in range(len(df)):

        codigo = str(df.index[i])
        nome = capitais.get(codigo, codigo)

        visible = [False] * len(df)
        visible[i] = True

        buttons.append(
            dict(
                label=nome,
                method="update",
                args=[
                    {"visible": visible},
                    {"title": f"DADOS SUS - {nome}"}
                ]
            )
        )

    fig.update_layout(
        updatemenus=[dict(active=0, buttons=buttons)],
        title=f"DADOS SUS - {nome_df}",
        xaxis_title="Tempo",
        yaxis_title="Atendimentos por 100k hab",
        width=1200,
        height=700
    )

    return fig

