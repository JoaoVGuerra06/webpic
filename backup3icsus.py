def plot_heatmap(dfs, nome_df, capitais):

    df = dfs[nome_df]

    nomes = [capitais.get(str(c), str(c)) for c in df.index]

    fig = go.Figure(
        data=go.Heatmap(
            z=df.values,
            x=df.columns,
            y=nomes,
            colorscale="turbo"
        )
    )

    fig.update_layout(
        title=f"DADOS SUS - {nome_df}",
        xaxis_title="Tempo",
        yaxis_title="Capital",
        width=1200,
        height=700
    )

    return fig

